BondList tJchain(int n_sites, double t, double J) {

  BondList bondlist;
  bondlist["T"] = t;
  bondlist["J"] = J;
  for (int s = 0; s < n_sites; ++s) {
    bondlist << Bond("HOP", "T", {s, (s + 1) % n_sites});
    bondlist << Bond("HB", "J", {s, (s + 1) % n_sites});
  }
  return bondlist;
}

