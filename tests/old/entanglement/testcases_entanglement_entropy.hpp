inline std::tuple<BondList, Couplings> tJchain_model(int nsites) {
  BondList bondlist;
  Couplings couplings;
  couplings["T"] = 1.0;
  couplings["J"] = 1.0;
  for (int s = 0; s < nsites - 1; ++s) {
    bondlist << Bond("HUBBARDHOP", "T", {s, s + 1});
    bondlist << Bond("HEISENBERG", "J", {s, s + 1});
  }
  return std::make_tuple(bondlist, couplings);
}

